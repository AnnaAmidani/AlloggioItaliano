package anna.alit.json;

import mic.organic.gateway.AbstractJson;
import mic.organic.gateway.Json;

/**
 *
 * @author mic
 * @created Feb 19, 2010
 */
public class AziendeJson extends AbstractJson implements Json
{
  private int id;
  private String tipologia;
  private String sigla;
  private String comune;
  private String denominazione;
  private String indirizzo;
  private String telefono;
  private String cellulare;
  private String fax;
  private String web;
  private String mail;
  private Integer punteggio;
  private Boolean responsabilita;

 /*
  *
  */
 public AziendeJson()
 {
   super();
 }



  public int getId()
  {
    return id;
  }

  public String getTipologia()
  {
    return tipologia;
  }


  public void setTipologia(String tipologia)
  {
    this.tipologia = tipologia;
  }


  public String getSigla()
  {
    return sigla;
  }


  public void setSigla(String sigla)
  {
    this.sigla = sigla;
  }


  public String getComune()
  {
    return comune;
  }


  public void setComune(String comune)
  {
    this.comune = comune;
  }


  public String getDenominazione()
  {
    return denominazione;
  }


  public void setDenominazione(String denominazione)
  {
    this.denominazione = denominazione;
  }


  public String getIndirizzo()
  {
    return indirizzo;
  }


  public void setIndirizzo(String indirizzo)
  {
    this.indirizzo = indirizzo;
  }


  public String getTelefono()
  {
    return telefono;
  }


  public void setTelefono(String telefono)
  {
    this.telefono = telefono;
  }


  public String getCellulare()
  {
    return cellulare;
  }


  public void setCellulare(String cellulare)
  {
    this.cellulare = cellulare;
  }


  public String getFax()
  {
    return fax;
  }


  public void setFax(String fax)
  {
    this.fax = fax;
  }


  public String getWeb()
  {
    return web;
  }


  public void setWeb(String web)
  {
    this.web = web;
  }


  public String getMail()
  {
    return mail;
  }


  public void setMail(String mail)
  {
    this.mail = mail;
  }


  public Integer getPunteggio()
  {
    return punteggio;
  }


  public void setPunteggio(Integer punteggio)
  {
    this.punteggio = punteggio;
  }


  public Boolean getResponsabilita()
  {
    return responsabilita;
  }


  public void setResponsabilita(Boolean responsabilita)
  {
    this.responsabilita = responsabilita;
  }





}
